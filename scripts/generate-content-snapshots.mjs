#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { loadColumnsDataset, loadDreamsDataset, loadDictionaryDataset } from './utils/content-data.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');
const outputPath = path.join(rootDir, 'client/src/generated/content-snapshots.ts');

const GUIDE_CATEGORY_LABELS = {
  luck: '개운법',
  basic: '사주 기초',
  relationship: '관계 · 궁합',
  health: '건강 · 운',
  money: '재물운',
  flow: '운의 흐름',
  career: '취업 · 커리어',
  love: '연애 · 결혼',
  family: '가족 · 자녀',
};

const DREAM_CATEGORY_LABELS = {
  animal: '동물',
  person: '인물 · 신체',
  nature: '자연 · 현상',
  object: '생활 · 사물',
  action: '행동 · 사건',
  emotion: '감정 · 관계',
  place: '장소 · 공간',
  other: '기타',
};

const DICTIONARY_CATEGORY_LABELS = {
  basic: '사주 기초',
  stem: '천간',
  branch: '지지',
  'ten-stem': '십신',
  sipsin: '십신',
  'evil-spirit': '신살',
  'luck-flow': '운의 흐름',
  relation: '관계 · 궁합',
  concept: '운세 개념',
  wealth: '재물 · 직업',
  health: '건강 · 신체',
  other: '기타',
};

function ensureDir(filePath) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
}

function normalizeSlug(value) {
  return String(value || '').trim().toLowerCase();
}

function stripHtml(value = '') {
  return String(value)
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<[^>]+>/g, ' ')
    .replace(/&nbsp;/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function truncate(value = '', maxLength = 160) {
  const text = String(value || '').trim();
  if (!text) return '';
  return text.length <= maxLength ? text : `${text.slice(0, Math.max(0, maxLength - 1)).trim()}…`;
}

function buildGuideIndex(rows) {
  return rows.map((row) => ({
    id: String(row.id),
    slug: normalizeSlug(row.slug || row.id),
    title: row.title || '',
    description: truncate(row.description || '', 180),
    category: row.category || 'luck',
    categoryLabel: GUIDE_CATEGORY_LABELS[row.category] || row.category || '운세 칼럼',
    author: row.author || '무운 역술팀',
    publishedDate: row.published_at || row.created_at || '',
    readTime: Number(row.read_time || 5),
    thumbnail: row.thumbnail_url || '',
  }));
}

function buildDreamIndex(rows) {
  return rows.map((row) => {
    const excerpt = truncate(stripHtml(row.meta_description || row.interpretation || ''), 160);
    const category = row.category || 'other';
    const grade = row.grade === 'great' || row.grade === 'bad' ? row.grade : 'good';
    return {
      id: String(row.id),
      keyword: row.keyword || '',
      slug: normalizeSlug(row.slug || row.id),
      excerpt,
      category,
      categoryLabel: DREAM_CATEGORY_LABELS[category] || '기타',
      grade,
      score: Number(row.score ?? 70),
      metaTitle: row.meta_title || `${row.keyword || ''} 꿈해몽 | 무운`,
      metaDescription: row.meta_description || excerpt,
      publishedDate: row.published_at || row.created_at || '',
    };
  });
}

function buildDictionaryIndex(rows) {
  return rows.map((row) => {
    const category = row.category || 'basic';
    return {
      id: String(row.id),
      slug: normalizeSlug(row.slug || row.id),
      category,
      categoryLabel: DICTIONARY_CATEGORY_LABELS[category] || category || '기타',
      title: row.title || '',
      subtitle: row.subtitle || undefined,
      summary: row.summary || '',
      originalMeaning: row.original_meaning || '',
      modernInterpretation: row.modern_interpretation || '',
      muunAdvice: row.muun_advice || '',
      tags: Array.isArray(row.tags) ? row.tags : [],
      metaTitle: row.meta_title || undefined,
      metaDescription: row.meta_description || undefined,
    };
  });
}

function makeFile({ generatedAt, sources, homeColumns, guideIndex, dreamIndex, dictionaryIndex }) {
  return `// This file is auto-generated by scripts/generate-content-snapshots.mjs\n// Do not edit manually. Generated at: ${generatedAt}\n// Sources: ${JSON.stringify(sources)}\n\nexport const BUILD_GENERATED_AT = ${JSON.stringify(generatedAt)};\nexport const BUILD_SNAPSHOT_SOURCES = ${JSON.stringify(sources, null, 2)} as const;\n\nexport const HOME_COLUMNS_PREVIEW = ${JSON.stringify(homeColumns, null, 2)} as const;\n\nexport const GUIDE_INDEX = ${JSON.stringify(guideIndex, null, 2)} as const;\n\nexport const DREAM_INDEX = ${JSON.stringify(dreamIndex, null, 2)} as const;\n\nexport const DICTIONARY_INDEX = ${JSON.stringify(dictionaryIndex, null, 2)} as const;\n\nexport type GuideIndexItem = (typeof GUIDE_INDEX)[number];\nexport type DreamIndexItem = (typeof DREAM_INDEX)[number];\nexport type DictionaryIndexItem = (typeof DICTIONARY_INDEX)[number];\n`;
}

async function main() {
  console.log('📚 Generating content snapshots for static/SEO rendering...');

  const [columnsResult, dreamsResult, dictionaryResult] = await Promise.all([
    loadColumnsDataset({ limit: 200 }),
    loadDreamsDataset({ limit: 600 }),
    loadDictionaryDataset({ limit: 300 }),
  ]);

  const guideIndex = buildGuideIndex(columnsResult.rows);
  const dreamIndex = buildDreamIndex(dreamsResult.rows);
  const dictionaryIndex = buildDictionaryIndex(dictionaryResult.rows);

  const generatedAt = new Date().toISOString();
  const homeColumns = guideIndex.slice(0, 3);
  const sources = {
    columns: columnsResult.source,
    dreams: dreamsResult.source,
    dictionary: dictionaryResult.source,
    columnsFallbackReason: columnsResult.fallbackReason || null,
    dreamsFallbackReason: dreamsResult.fallbackReason || null,
    dictionaryFallbackReason: dictionaryResult.fallbackReason || null,
  };

  ensureDir(outputPath);
  fs.writeFileSync(
    outputPath,
    makeFile({ generatedAt, sources, homeColumns, guideIndex, dreamIndex, dictionaryIndex }),
    'utf8',
  );

  console.log(`✅ Content snapshots written to ${path.relative(rootDir, outputPath)}`);
  console.log(`   Columns: ${guideIndex.length} (${columnsResult.source})`);
  console.log(`   Dreams: ${dreamIndex.length} (${dreamsResult.source})`);
  console.log(`   Dictionary: ${dictionaryIndex.length} (${dictionaryResult.source})`);
}

main().catch((error) => {
  console.error('❌ Failed to generate content snapshots:', error instanceof Error ? error.message : error);
  process.exit(1);
});
