async function fetchActiveTheme() {
  const response = await fetch(
    `${SUPABASE_URL}/rest/v1/design_themes?is_active=eq.true&limit=1`,
    {
      headers: {
        'apikey': SUPABASE_ANON_KEY,
        'Authorization': `Bearer ${SUPABASE_ANON_KEY}`,
        'Content-Type': 'application/json',
      },
    }
  );

  if (!response.ok) {
    throw new Error(`Supabase API error: ${response.status} ${response.statusText}`);
  }

  const themes = await response.json();
  return themes.length > 0 ? themes[0] : null;
}

function generateCss(theme) {
  const lines = [
    `/* ============================================================`,
    ` * 자동 생성 파일 - 직접 편집하지 마세요`,
    ` * Generated by inject-design-tokens.mjs`,
    ` * Theme: ${theme.name}`,
    ` * Generated at: ${new Date().toISOString()}`,
    ` * ============================================================ */`,
    ``,
    `:root {`,
  ];

  // 색상 변수
  if (theme.colors && Object.keys(theme.colors).length > 0) {
    lines.push(`  /* === 색상 토큰 (Theme: ${theme.name}) === */`);
    for (const [key, value] of Object.entries(theme.colors)) {
      lines.push(`  ${key}: ${value};`);
    }
    lines.push('');
  }

  // 타이포그래피 변수
  if (theme.typography && Object.keys(theme.typography).length > 0) {
    lines.push(`  /* === 타이포그래피 토큰 === */`);
    for (const [key, value] of Object.entries(theme.typography)) {
      lines.push(`  ${key}: ${value};`);
    }
    lines.push('');
  }

  // 그라디언트 변수
  if (theme.gradients && Object.keys(theme.gradients).length > 0) {
    lines.push(`  /* === 그라디언트 토큰 === */`);
    for (const [key, value] of Object.entries(theme.gradients)) {
      lines.push(`  ${key}: ${value};`);
    }
    lines.push('');
  }

  // 컴포넌트 토큰 (섹션별 플랫 주입)
  if (theme.component_tokens && Object.keys(theme.component_tokens).length > 0) {
    const componentTokens = theme.component_tokens;
    const SECTION_LABELS = {
      global: '전역 Shape/Radius',
      button: '버튼',
      card: '카드',
      input: '인풋/텍스트에어리어',
      badge: '배지',
      'bottom-nav': '바텀 네비게이션',
      gnb: 'GNB 상단 네비게이션',
      dialog: '다이얼로그/모달',
      tabs: '탭',
      toast: '토스트/알림',
      select: '셀렉트/드롭다운',
      avatar: '아바타',
      divider: '구분선',
      scrollbar: '스크롤바',
    };

    let hasComponentTokens = false;
    for (const [sectionId, group] of Object.entries(componentTokens)) {
      if (!group || Object.keys(group).length === 0) continue;
      if (!hasComponentTokens) {
        lines.push(`  /* === 컴포넌트 토큰 === */`);
        hasComponentTokens = true;
      }
      const label = SECTION_LABELS[sectionId] || sectionId;
      lines.push(`  /* -- ${label} -- */`);
      for (const [key, value] of Object.entries(group)) {
        lines.push(`  ${key}: ${value};`);
      }
      lines.push('');
    }
  }

  lines.push(`}`);
  return lines.join('\n');
}

function generateFallbackCss() {
  return `/* ============================================================
 * 자동 생성 파일 - 직접 편집하지 마세요
 * Generated by inject-design-tokens.mjs
 * Status: FALLBACK (활성 테마 없음 또는 Supabase 연결 실패)
 * Generated at: ${new Date().toISOString()}
 * ============================================================ */

/* 활성 테마가 없거나 Supabase 연결에 실패한 경우 기본값 사용 */
/* index.css의 기본 변수가 적용됩니다 */
`;
}

async function main() {
  console.log('🎨 디자인 토큰 주입 시작...');
  console.log(`   Supabase URL: ${SUPABASE_URL}`);

  try {
    const theme = await fetchActiveTheme();

    if (!theme) {
      console.warn('⚠️  활성화된 테마가 없습니다. 기본 CSS 변수를 사용합니다.');
      fs.writeFileSync(OUTPUT_PATH, generateFallbackCss(), 'utf-8');
      console.log(`✅ 폴백 CSS 파일 생성: ${OUTPUT_PATH}`);
      return;
    }

    console.log(`✅ 활성 테마 발견: "${theme.name}"`);
    const componentTokenCount = theme.component_tokens
      ? Object.values(theme.component_tokens).reduce((acc, group) => acc + (group ? Object.keys(group).length : 0), 0)
      : 0;
    console.log(`   색상 토큰: ${Object.keys(theme.colors || {}).length}개`);
    console.log(`   타이포그래피 토큰: ${Object.keys(theme.typography || {}).length}개`);
    console.log(`   그라디언트 토큰: ${Object.keys(theme.gradients || {}).length}개`);
    console.log(`   컴포넌트 토큰: ${componentTokenCount}개`);

    const css = generateCss(theme);
    fs.writeFileSync(OUTPUT_PATH, css, 'utf-8');
    console.log(`✅ 디자인 토큰 CSS 주입 완료: ${OUTPUT_PATH}`);
    console.log(`   총 ${css.split('\n').length}줄 생성`);

  } catch (error) {
    console.error('❌ 디자인 토큰 주입 실패:', error.message);
    console.warn('   폴백 CSS를 사용합니다.');
    fs.writeFileSync(OUTPUT_PATH, generateFallbackCss(), 'utf-8');
    // 빌드를 중단하지 않음 (폴백 사용)
    process.exit(0);
  }
}

main();
